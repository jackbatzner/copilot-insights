/**
 * GET /api/practice/challenge
 * Fetch a random past prompt that scored poorly for the rewrite challenge.
 */
app.get("/api/practice/challenge", (req, res) => {
  try {
    const tf = req.query.timeframe || "90d";
    if (typeof tf !== "string" || !TIMEFRAME_RE.test(tf)) {
      return res.status(400).json({ error: "Invalid timeframe parameter" });
    }
    const since = parseSince(tf);
    const result = analyzeRecent({ since, limit: 200, excludeIds: hiddenSessions });

    // Collect user turns that had redirection patterns
    const candidates = [];
    for (const s of result.sessions) {
      for (const r of s.redirections) {
        if (r.weight >= 2 && r.message && r.message.length >= 20) {
          // Skip auto-generated prompts ΓÇö no coaching value
          if (isAutoGenerated(r.message)) continue;
          const analysis = analyzePrompt(r.message);
          if (analysis.score < 70) {
            candidates.push({
              originalPrompt: r.message,
              score: analysis.score,
              grade: analysis.grade,
              patterns: analysis.patterns,
              categories: analysis.categories,
              suggestions: analysis.suggestions,
              sessionId: s.session.id,
              turnIndex: r.turnIndex,
            });
          }
        }
      }
    }

    if (candidates.length === 0) {
      return res.json({
        challenge: null,
        message: "No low-scoring prompts found ΓÇö your prompting is already strong! Try the sandbox instead.",
      });
    }

    // Pick a random candidate
    const challenge = candidates[Math.floor(Math.random() * candidates.length)];
    res.json({ challenge });
  } catch (err) {
    handleRouteError(res, err, "unknown");
  }
});

/**
 * GET /api/practice/weaknesses
 * Analyze the user's recent prompts and return recommended challenge tags.
 * Scans real session turns, runs heuristics, and counts which quality signals
 * are most frequently missing ΓÇö then maps those to library tags.
 */
app.get("/api/practice/weaknesses", (req, res) => {
  try {
    const tf = req.query.timeframe || "90d";
    if (typeof tf !== "string" || !TIMEFRAME_RE.test(tf)) {
      return res.status(400).json({ error: "Invalid timeframe parameter" });
    }
    const since = parseSince(tf);
    const result = analyzeRecent({ since, limit: 200, excludeIds: hiddenSessions });

    // Aggregate heuristic gaps across all user turns
    const gaps = {
      "no-files": 0, "no-constraints": 0, "no-criteria": 0,
      "no-context": 0, "no-examples": 0, "no-format": 0,
      "no-steps": 0, vague: 0, correction: 0, frustration: 0, rollback: 0,
    };
    let totalTurns = 0;

    for (const s of result.sessions) {
      for (const r of s.redirections) {
        if (!r.message || r.message.length < 10) continue;
        // Skip auto-generated prompts
        if (isAutoGenerated(r.message)) continue;
        totalTurns++;
        const analysis = analyzePrompt(r.message);
        const h = analysis.heuristics;

        // Count missing quality signals
        if (!h.hasFilePaths && h.wordCount >= 5) gaps["no-files"]++;
        if (!h.hasConstraints && h.wordCount >= 8) gaps["no-constraints"]++;
        if (!h.hasCriteria && h.wordCount >= 10) gaps["no-criteria"]++;
        if (!h.hasContext && h.wordCount >= 10) gaps["no-context"]++;
        if (!h.hasExamples && h.wordCount >= 12) gaps["no-examples"]++;
        if (!h.hasOutputFormat && h.wordCount >= 12) gaps["no-format"]++;
        if (!h.hasSteps && h.wordCount >= 15) gaps["no-steps"]++;
        if (h.charCount < 30) gaps.vague++;

        // Count redirection categories
        for (const p of analysis.patterns) {
          if (p.category === "explicit_correction") gaps.correction++;
          else if (p.category === "frustration") gaps.frustration++;
          else if (p.category === "rollback") gaps.rollback++;
        }
      }
    }

    // Rank by frequency, return top tags with counts
    const ranked = Object.entries(gaps)
      .filter(([, count]) => count > 0)
      .sort((a, b) => b[1] - a[1])
      .map(([tag, count]) => ({ tag, count, pct: totalTurns ? Math.round((count / totalTurns) * 100) : 0 }));

    res.json({ weaknesses: ranked, totalTurns });
  } catch (err) {
    handleRouteError(res, err, "unknown");
  }
});

/**
 * GET /api/practice/library
 * Return the curated challenge library with optional tag filtering.
 * Query params:
 *   tag ΓÇö filter to prompts matching this tag (can repeat: ?tag=vague&tag=no-files)
 *   random ΓÇö if "1", return a single random prompt instead of the full list
 */
app.get("/api/practice/library", (req, res) => {
  try {
    const tags = [].concat(req.query.tag || []).filter((t) => typeof t === "string" && VALID_LIBRARY_TAGS.has(t));
    let pool = CHALLENGE_LIBRARY;
    if (tags.length > 0) {
      pool = pool.filter((c) => tags.some((t) => c.tags.includes(t)));
    }
    if (req.query.random === "1") {
      if (pool.length === 0) return res.json({ challenge: null, total: 0 });
      const pick = pool[Math.floor(Math.random() * pool.length)];
      const analysis = analyzePrompt(pick.prompt);
      return res.json({
        challenge: {
          originalPrompt: pick.prompt,
          tags: pick.tags,
          hint: pick.hint,
          score: analysis.score,
          grade: analysis.grade,
          patterns: analysis.patterns,
          categories: analysis.categories,
          suggestions: analysis.suggestions,
        },
        total: pool.length,
      });
    }
    res.json({ challenges: pool, total: pool.length });
  } catch (err) {
    handleRouteError(res, err, "GET /api/practice/library");
  }
});


// ΓöÇΓöÇ Insight generation helper ΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇΓöÇ

function generateInsights(analysisResult, topPatterns) {
  const { aggregate } = analysisResult;
  const insights = [];

  // High correction rate
  if (aggregate.avgRedirectionRate > 0.3) {
    insights.push({
      type: "warning",
      title: "High Correction Rate",
      body: `${(aggregate.avgRedirectionRate * 100).toFixed(0)}% of your turns are corrections. Try providing more upfront context ΓÇö include file paths, expected behavior, and constraints in your initial prompt.`,
      category: "general",
    });
  }

  // Explicit corrections dominate
  const corrections = aggregate.categoryTotals.explicit_correction;
  if (corrections && corrections.count > 3) {
    insights.push({
      type: "tip",
      title: "Frequent Explicit Corrections",
      body: `You're correcting the agent ${corrections.count} times across sessions. Be more specific about what you want ΓÇö include examples of the desired output or reference existing patterns in the codebase.`,
      category: "explicit_correction",
    });
  }

  // Frustration signals
  const frustration = aggregate.categoryTotals.frustration;
  if (frustration && frustration.count > 2) {
    insights.push({
      type: "warning",
      title: "Frustration Detected",
      body: `${frustration.count} frustration signals found. When the agent isn't understanding you, try breaking the task into smaller steps rather than repeating the same instruction.`,
      category: "frustration",
    });
  }

  // Course changes
  const courseChanges = aggregate.categoryTotals.course_change;
  if (courseChanges && courseChanges.count > 3) {
    insights.push({
      type: "tip",
      title: "Frequent Course Changes",
      body: `${courseChanges.count} mid-task pivots detected. Consider spending more time planning before starting ΓÇö outline the approach first, then implement. Use plan mode.`,
      category: "course_change",
    });
  }

  // Rollbacks
  const rollbacks = aggregate.categoryTotals.rollback;
  if (rollbacks && rollbacks.count > 2) {
    insights.push({
      type: "warning",
      title: "Frequent Rollback Requests",
      body: `${rollbacks.count} rollback requests across sessions. Try reviewing changes incrementally ΓÇö ask the agent to make one change at a time so you can validate before proceeding.`,
      category: "rollback",
    });
  }

  // Top pattern insight
  if (topPatterns.length > 0) {
    const top = topPatterns[0];
    insights.push({
      type: "info",
      title: `Most Common Pattern: "${top.label}"`,
      body: `This pattern appeared ${top.count} times. Example: "${top.examples[0]?.message || "N/A"}"`,
      category: top.category,
    });
  }

  // Positive feedback
  if (aggregate.avgRedirectionRate < 0.1 && aggregate.sessionsAnalyzed > 3) {
    insights.push({
      type: "success",
      title: "Great Prompting!",
      body: `Only ${(aggregate.avgRedirectionRate * 100).toFixed(0)}% redirection rate ΓÇö you're communicating clearly with the agent. Keep it up!`,
      category: "general",
    });
  }

  return insights;
}

