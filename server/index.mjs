const DEVPLAN_FILE = join(homedir(), ".copilot", "copilot-insights-devplan.json");
const JOURNAL_FILE = join(homedir(), ".copilot", "copilot-insights-journal.md");

function readDevPlan() {
  try {
    if (!existsSync(DEVPLAN_FILE)) return { goals: [] };
    const parsed = JSON.parse(readFileSync(DEVPLAN_FILE, "utf-8"));
    if (!parsed || typeof parsed !== "object" || !Array.isArray(parsed.goals)) return { goals: [] };
    return parsed;
  } catch {
    return { goals: [] };
  }
}

function writeDevPlan(data) {
  const dir = dirname(DEVPLAN_FILE);
  if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
  writeFileSync(DEVPLAN_FILE, JSON.stringify(data, null, 2), "utf-8");
  regenerateJournal(data);
}

function regenerateJournal(plan) {
  const lines = [
    "# Copilot Insights — Development Journal",
    "",
    `_Last updated: ${new Date().toISOString()}_`,
    "",
  ];

  const active = plan.goals.filter((g) => g.status === "active");
  const sufficient = plan.goals.filter((g) => g.status === "sufficient");

  if (active.length > 0) {
    lines.push("## 🎯 Active Goals", "");
    for (const g of active) {
      lines.push(`### ${g.title}`);
      lines.push(`- **Pillar:** ${g.pillar}`);
      lines.push(`- **Added:** ${new Date(g.addedAt).toLocaleDateString()}`);
      if (g.baselineScore !== null && g.baselineScore !== undefined) lines.push(`- **Baseline Score:** ${g.baselineScore}`);
      if (g.latestScore !== null && g.latestScore !== undefined) lines.push(`- **Latest Score:** ${g.latestScore}`);
      if (g.description) lines.push(`- **Details:** ${g.description}`);
      if (g.notes && g.notes.length > 0) {
        lines.push("", "**Notes & Insights:**");
        for (const n of g.notes) {
          lines.push(`- _${new Date(n.createdAt).toLocaleDateString()}:_ ${n.text}`);
        }
      }
      lines.push("");
    }
  }

  if (sufficient.length > 0) {
    lines.push("## ✅ Development Sufficient", "");
    for (const g of sufficient) {
      lines.push(`### ${g.title}`);
      lines.push(`- **Pillar:** ${g.pillar}`);
      if (
        g.baselineScore !== null && g.baselineScore !== undefined
        && g.latestScore !== null && g.latestScore !== undefined
      ) {
        lines.push(`- **Progress:** ${g.baselineScore} → ${g.latestScore}`);
      }
      if (g.completedAt) lines.push(`- **Completed:** ${new Date(g.completedAt).toLocaleDateString()}`);
      if (g.notes && g.notes.length > 0) {
        lines.push("", "**Notes & Insights:**");
        for (const n of g.notes) {
          lines.push(`- _${new Date(n.createdAt).toLocaleDateString()}:_ ${n.text}`);
        }
      }
      lines.push("");
    }
  }

  if (active.length === 0 && sufficient.length === 0) {
    lines.push("_No development goals yet. Add tips from the Skill Building page._", "");
  }

  lines.push("---", "", "_Auto-generated by Copilot Insights. Edit goals from the dashboard._", "");

  try {
    const dir = dirname(JOURNAL_FILE);
    if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
    writeFileSync(JOURNAL_FILE, lines.join("\n"), "utf-8");
  } catch (err) {
    console.error("[journal] Failed to write journal:", err.message);
  }
}

