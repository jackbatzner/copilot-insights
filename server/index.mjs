/**
 * Convert a timeframe string (7d, 30d, 90d, all) to an ISO date.
 */
function parseSince(timeframe) {
  if (!timeframe || timeframe === "all") return undefined;
  const match = timeframe.match(/^(\d+)d$/);
  if (!match) return undefined;
  const days = parseInt(match[1]);
  const d = new Date();
  d.setDate(d.getDate() - days);
  return d.toISOString();
}


/** Accepted timeframe patterns: digits + d/w/m/y, or "all". */
const TIMEFRAME_RE = /^(?:\d{1,4}[dwmy]|all)$/;

/**
 * Validate the `repo` query parameter — must be a non-empty string if present.
 * Returns the validated repo string or undefined.
 */
function validateRepo(req, res) {
  const repo = req.query.repo;
  if (repo === undefined || repo === "") return undefined;
  if (typeof repo !== "string") {
    res.status(400).json({ error: "Invalid 'repo' parameter — must be a string" });
    return null;
  }
  return repo;
}

/**
 * Validate and parse the `timeframe` query parameter.
 * Returns the ISO date string, undefined (for "all"), or null on validation failure.
 */
function validateTimeframe(req, res) {
  const tf = req.query.timeframe;
  if (!tf || tf === "all") return undefined;
  if (typeof tf !== "string" || !TIMEFRAME_RE.test(tf)) {
    res.status(400).json({ error: "Invalid 'timeframe' parameter — use format like '7d', '30d', or 'all'" });
    return null;
  }
  return parseSince(tf);
}

/**
 * Standardized route error handler — logs context and sends 500.
 */
function handleRouteError(res, err, route) {
  log.error(`[${route}]`, err);
  res.status(500).json({ error: "Internal server error" });
}
