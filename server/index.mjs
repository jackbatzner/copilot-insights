/**
 * Convert a timeframe string (7d, 30d, 90d, all) to an ISO date.
 */
function parseSince(timeframe) {
  if (!timeframe || timeframe === "all") return undefined;
  const match = timeframe.match(/^(\d+)d$/);
  if (!match) return undefined;
  const days = parseInt(match[1]);
  const d = new Date();
  d.setDate(d.getDate() - days);
  return d.toISOString();
}


/** Accepted timeframe patterns: digits + d/w/m/y, or "all". */
const TIMEFRAME_RE = /^(?:\d{1,4}[dwmy]|all)$/;

/**
 * Validate the `repo` query parameter — must be a non-empty string if present.
 * Returns the validated repo string or undefined.
 */
function validateRepo(req, res) {
  const repo = req.query.repo;
  if (repo === undefined || repo === "") return undefined;
  if (typeof repo !== "string") {
    res.status(400).json({ error: "Invalid 'repo' parameter — must be a string" });
    return null;
  }
  return repo;
}

/**
 * Validate and parse the `timeframe` query parameter.
 * Returns the ISO date string, undefined (for "all"), or null on validation failure.
 */
function validateTimeframe(req, res) {
  const tf = req.query.timeframe;
  if (!tf || tf === "all") return undefined;
  if (typeof tf !== "string" || !TIMEFRAME_RE.test(tf)) {
    res.status(400).json({ error: "Invalid 'timeframe' parameter — use format like '7d', '30d', or 'all'" });
    return null;
  }
  return parseSince(tf);
}

/**
 * Validate a direct ISO `since` query parameter.
 */
function validateSince(req, res) {
  const since = req.query.since;
  if (since === undefined || since === "") return undefined;
  if (typeof since !== "string") {
    res.status(400).json({ error: "Invalid 'since' parameter — must be an ISO date string" });
    return null;
  }
  const parsed = new Date(since);
  if (Number.isNaN(parsed.getTime())) {
    res.status(400).json({ error: "Invalid 'since' parameter — must be an ISO date string" });
    return null;
  }
  return parsed.toISOString();
}

/**
 * Build a standard { repo, since, excludeIds } filter object for analytics routes.
 */
function buildFilterOpts(req, res) {
  const repo = validateRepo(req, res);
  if (repo === null) return null;

  const since = req.query.since !== undefined
    ? validateSince(req, res)
    : validateTimeframe(req, res);
  if (since === null) return null;

  const excludeIds = new Set(hiddenSessions);
  if (req.query.excludeIds !== undefined && req.query.excludeIds !== "") {
    if (typeof req.query.excludeIds !== "string") {
      res.status(400).json({ error: "Invalid 'excludeIds' parameter — must be a comma-separated string" });
      return null;
    }
    for (const id of req.query.excludeIds.split(",").map((value) => value.trim()).filter(Boolean)) {
      excludeIds.add(id);
    }
  }

  return { repo, since, excludeIds };
}

function parseTimeframeQuery(query) {
  const repo = typeof query.repo === "string" && query.repo !== "" ? query.repo : undefined;
  if (typeof query.since === "string" && query.since !== "") {
    const parsed = new Date(query.since);
    return {
      repo,
      since: Number.isNaN(parsed.getTime()) ? undefined : parsed.toISOString(),
    };
  }
  if (typeof query.timeframe === "string" && query.timeframe !== "" && query.timeframe !== "all") {
    return { repo, since: parseSince(query.timeframe) };
  }
  return { repo, since: undefined };
}

/**
 * Standardized route error handler — logs context and sends user-friendly 500.
 */
function handleRouteError(res, err, route) {
  log.error(`[${route}]`, err);

  // Surface actionable DB errors to the user
  if (err.message?.includes("session database") || err.message?.includes("Session store") || err.message?.includes("SQLITE")) {
    return res.status(500).json({
      error: "Couldn't access the Copilot session database. Make sure Copilot CLI is installed and you've completed at least one session.",
    });
  }

  res.status(500).json({ error: "Something went wrong — check the server logs for details." });
}
