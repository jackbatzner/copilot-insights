const HIDDEN_FILE = join(homedir(), ".copilot", "copilot-insights-hidden.json");
const MAX_HIDDEN = 5000;
const UUID_RE = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

function loadHiddenSessions() {
  try {
    const raw = readFileSync(HIDDEN_FILE, "utf-8");
    const parsed = JSON.parse(raw);
    if (Array.isArray(parsed)) {
      const valid = parsed.filter((id) => typeof id === "string" && UUID_RE.test(id));
      return new Set(valid.slice(0, MAX_HIDDEN));
    }
  } catch {
    // File missing, corrupted, or unreadable — start fresh
  }
  return new Set();
}

function saveHiddenSessions(set) {
  try {
    mkdirSync(join(homedir(), ".copilot"), { recursive: true });
    writeFileSync(HIDDEN_FILE, JSON.stringify([...set]), "utf-8");
  } catch (err) {
    log.error("[hidden-sessions] Failed to save:", err.message);
  }
}

const hiddenSessions = loadHiddenSessions();

// -- Session Intent Tags (persisted to disk) ----------------------
const TAGS_FILE = join(homedir(), ".copilot", "copilot-insights-tags.json");

// Valid intent tags
const VALID_INTENTS = new Set(["build", "explore", "iterate", "debug"]);

function loadSessionTags() {
  try {
    const raw = readFileSync(TAGS_FILE, "utf-8");
    const parsed = JSON.parse(raw);
    if (typeof parsed === "object" && !Array.isArray(parsed)) {
      // Validate entries
      const clean = {};
      for (const [id, tag] of Object.entries(parsed)) {
        if (UUID_RE.test(id) && VALID_INTENTS.has(tag)) {
          clean[id] = tag;
        }
      }
      return clean;
    }
  } catch {
    // File missing or corrupted — start fresh
  }
  return {};
}

function saveSessionTags(tags) {
  try {
    mkdirSync(join(homedir(), ".copilot"), { recursive: true });
    writeFileSync(TAGS_FILE, JSON.stringify(tags, null, 2), "utf-8");
  } catch (err) {
    log.error("[session-tags] Failed to save:", err.message);
  }
}

