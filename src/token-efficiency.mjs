// Token efficiency analysis — computes per-session and aggregate token metrics
// using real token data (from JSONL) or estimation (from text content).

import { getSessionTurns } from "./db.mjs";
import { matchPatterns } from "./patterns.mjs";
import { isAutoGenerated } from "./autogenerated.mjs";

/**
 * Analyze token efficiency for a single session.
 *
 * @param {string} sessionId - Session UUID.
 * @param {object} tokenData - Token data from token-reader.mjs's getTokenData().
 * @param {Array} [turns] - Pre-fetched turns array (optional; fetched from DB if not provided).
 * @returns {object|null} Token efficiency report, or null if insufficient data.
 */
export function analyzeTokenEfficiency(sessionId, tokenData, turns) {
  if (!tokenData || !tokenData.turns || tokenData.turns.length === 0) return null;

  const sessionTurns = turns || getSessionTurns(sessionId);
  if (!sessionTurns || sessionTurns.length === 0) return null;

  const { totals, source: dataSource } = tokenData;

  // Identify wasted turns — those that are followed by a redirection pattern
  let wastedTokens = 0;
  let wastedTurns = 0;
  const turnAnalysis = [];

  for (let i = 0; i < sessionTurns.length; i++) {
    const turn = sessionTurns[i];
    const tokenTurn = tokenData.turns[i];
    if (!tokenTurn) continue;

    const isAuto = turn.user_message && isAutoGenerated(turn.user_message);
    let isWasted = false;

    if (!isAuto && turn.user_message) {
      // A turn is "wasted" if the NEXT user message contains a redirection pattern
      // (meaning the current turn's output wasn't satisfactory)
      const nextTurn = sessionTurns[i + 1];
      if (nextTurn && nextTurn.user_message && !isAutoGenerated(nextTurn.user_message)) {
        const matches = matchPatterns(nextTurn.user_message);
        if (matches.length > 0) {
          isWasted = true;
          wastedTokens += tokenTurn.totalTokens;
          wastedTurns++;
        }
      }
    }

    turnAnalysis.push({
      turnIndex: tokenTurn.turnIndex ?? i,
      totalTokens: tokenTurn.totalTokens,
      promptTokens: tokenTurn.promptTokens,
      completionTokens: tokenTurn.completionTokens,
      model: tokenTurn.model,
      isWasted,
      isAutoGenerated: isAuto || false,
    });
  }

  const totalTokens = totals.totalTokens;
  const productiveTokens = totalTokens - wastedTokens;
  const efficiencyRatio = totalTokens > 0
    ? Math.round((productiveTokens / totalTokens) * 100) / 100
    : 1;

  // Tokens per (non-auto-generated) turn
  const userTurnCount = turnAnalysis.filter((t) => !t.isAutoGenerated).length;
  const tokensPerTurn = userTurnCount > 0
    ? Math.round(totalTokens / userTurnCount)
    : 0;

  // Context window budget (assume ~200k for modern models)
  const MODEL_CONTEXT_WINDOWS = {
    "claude-sonnet": 200000,
    "claude-opus": 200000,
    "claude-haiku": 200000,
    "gpt-4": 128000,
    "gpt-4o": 128000,
    "gpt-3.5": 16000,
  };

  // Detect model from token data
  const detectedModel = tokenData.turns.find((t) => t.model)?.model || null;
  let contextWindowSize = 200000; // default
  if (detectedModel) {
    for (const [key, size] of Object.entries(MODEL_CONTEXT_WINDOWS)) {
      if (detectedModel.toLowerCase().includes(key)) {
        contextWindowSize = size;
        break;
      }
    }
  }

  const tokenBudgetUsage = Math.round((totalTokens / contextWindowSize) * 100) / 100;

  // Grade
  let grade;
  if (efficiencyRatio >= 0.9) grade = { label: "Excellent", emoji: "🌟", color: "#3fb950" };
  else if (efficiencyRatio >= 0.75) grade = { label: "Good", emoji: "👍", color: "#58a6ff" };
  else if (efficiencyRatio >= 0.6) grade = { label: "Fair", emoji: "📐", color: "#d29922" };
  else grade = { label: "Needs Work", emoji: "🔧", color: "#f85149" };

  return {
    grade,
    dataSource,
    totalTokens,
    promptTokens: totals.promptTokens,
    completionTokens: totals.completionTokens,
    thinkingTokens: totals.thinkingTokens || 0,
    wastedTokens,
    productiveTokens,
    efficiencyRatio,
    tokensPerTurn,
    tokenBudgetUsage,
    contextWindowSize,
    model: detectedModel,
    turnCount: userTurnCount,
    wastedTurns,
    turns: turnAnalysis,
  };
}

/**
 * Analyze token efficiency across multiple sessions.
 *
 * @param {Array<{session: object, tokenData: object, turns: Array}>} sessionsData
 * @returns {object} Aggregate token efficiency report.
 */
export function analyzeTokenEfficiencyBatch(sessionsData) {
  const results = [];
  let totalTokensAll = 0;
  let totalWastedAll = 0;
  let totalSessionsAnalyzed = 0;
  const categoryWaste = {};

  for (const { session, tokenData, turns } of sessionsData) {
    const eff = analyzeTokenEfficiency(session.id, tokenData, turns);
    if (!eff) continue;

    totalSessionsAnalyzed++;
    totalTokensAll += eff.totalTokens;
    totalWastedAll += eff.wastedTokens;

    // Track waste by redirection category
    for (let i = 0; i < turns.length; i++) {
      const _turn = turns[i];
      const nextTurn = turns[i + 1];
      if (nextTurn && nextTurn.user_message && !isAutoGenerated(nextTurn.user_message)) {
        const matches = matchPatterns(nextTurn.user_message);
        for (const m of matches) {
          if (!categoryWaste[m.category]) categoryWaste[m.category] = 0;
          const tokenTurn = tokenData.turns[i];
          if (tokenTurn) categoryWaste[m.category] += tokenTurn.totalTokens;
        }
      }
    }

    results.push({
      sessionId: session.id,
      repository: session.repository,
      summary: session.summary,
      createdAt: session.created_at,
      efficiency: eff,
    });
  }

  const avgEfficiency = totalSessionsAnalyzed > 0
    ? Math.round(((totalTokensAll - totalWastedAll) / totalTokensAll) * 100)
    : 100;

  const avgTokensPerSession = totalSessionsAnalyzed > 0
    ? Math.round(totalTokensAll / totalSessionsAnalyzed)
    : 0;

  // Sort waste categories by token cost
  const wasteByCategory = Object.entries(categoryWaste)
    .map(([category, tokens]) => ({ category, tokens }))
    .sort((a, b) => b.tokens - a.tokens);

  return {
    aggregate: {
      sessionsAnalyzed: totalSessionsAnalyzed,
      totalTokens: totalTokensAll,
      totalWasted: totalWastedAll,
      avgEfficiency,
      avgTokensPerSession,
      wasteByCategory,
    },
    // Sessions sorted by worst efficiency first
    sessions: results.sort((a, b) =>
      a.efficiency.efficiencyRatio - b.efficiency.efficiencyRatio
    ),
  };
}

/**
 * Compute a token efficiency pillar score (0-100).
 * Based on waste ratio, tokens per successful outcome, and trend improvement.
 *
 * @param {object} batchResult - Result from analyzeTokenEfficiencyBatch().
 * @returns {number} Score from 0-100.
 */
export function computeTokenEfficiencyScore(batchResult) {
  if (!batchResult || batchResult.aggregate.sessionsAnalyzed === 0) return 50; // neutral

  const { avgEfficiency } = batchResult.aggregate;
  // avgEfficiency is already a percentage (0-100)
  return Math.max(0, Math.min(100, avgEfficiency));
}
