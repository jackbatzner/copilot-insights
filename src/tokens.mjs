// ── Live Pricing Fetcher ─────────────────────────────────────
// Fetches current pricing from GitHub Copilot docs on page load.
// Caches for 1 hour. Falls back to hardcoded MODEL_PRICING on failure.

const PRICING_URL = "https://docs.github.com/en/copilot/reference/copilot-billing/models-and-pricing";
const CACHE_TTL_MS = 24 * 60 * 60 * 1000; // 24 hours

let livePricingCache = null;
let livePricingFetchedAt = 0;

/**
 * Normalize a display model name to a slug (e.g., "Claude Sonnet 4.6" → "claude-sonnet-4.6").
 */
function normalizeModelName(displayName) {
  return displayName
    .replace(/\[.*?\]/g, "")   // remove footnote refs like [1]
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-");
}

/**
 * Parse pricing tables from the GitHub docs page (HTML or simplified text).
 * Handles both raw HTML (<td>$2.50</td>) and simplified markdown text.
 * Standard columns: Model | Release status | Category | Input | Cached input | Output
 * Anthropic adds: Cache write between Cached input and Output.
 */
function parsePricingFromPage(text) {
  const models = {};

  // Normalize HTML entities: $ → $, etc.
  const normalized = text
    .replace(/&#?\w+;/g, (m) => {
      if (m === "$" || m === "$") return "$";
      if (m === "&amp;") return "&";
      if (m === "&lt;") return "<";
      if (m === "&gt;") return ">";
      return m;
    });

  // Find the pricing tables section — use heading ids to skip nav sidebars
  let pricingStart = normalized.indexOf('id="pricing-tables"');
  if (pricingStart === -1) pricingStart = normalized.lastIndexOf("Pricing tables");
  if (pricingStart === -1) return null;
  let pricingEnd = normalized.indexOf('id="code-completions"', pricingStart);
  if (pricingEnd === -1) pricingEnd = normalized.indexOf("Code completions", pricingStart + 100);
  const section = normalized.slice(pricingStart, pricingEnd > 0 ? pricingEnd : undefined);

  const vendors = [
    { name: "OpenAI", hasCacheWrite: false },
    { name: "Anthropic", hasCacheWrite: true },
    { name: "Google", hasCacheWrite: false },
    { name: "xAI", hasCacheWrite: false },
    { name: "Fine-tuned", hasCacheWrite: false },
  ];

  // Extract table rows — works for both HTML <tr>...</tr> and text lines
  const rowPattern = /<tr[^>]*>([\s\S]*?)<\/tr>/gi;
  const cellPattern = /<td[^>]*>([\s\S]*?)<\/td>/gi;

  for (const vendor of vendors) {
    const vendorIdx = section.indexOf(vendor.name);
    if (vendorIdx === -1) continue;

    let endIdx = section.length;
    for (const other of vendors) {
      if (other.name === vendor.name) continue;
      const otherIdx = section.indexOf(other.name, vendorIdx + vendor.name.length);
      if (otherIdx > 0 && otherIdx < endIdx) endIdx = otherIdx;
    }

    const vendorSection = section.slice(vendorIdx, endIdx);

    // Try HTML table parsing first
    const rows = [...vendorSection.matchAll(rowPattern)];
    if (rows.length > 0) {
      for (const row of rows) {
        const cells = [...row[1].matchAll(cellPattern)].map(
          (c) => c[1].replace(/<sup[\s\S]*?<\/sup>/gi, "").replace(/<[^>]+>/g, "").trim()
        );
        if (cells.length < 6) continue;

        const modelName = cells[0];
        const category = cells[2];
        const prices = cells.slice(3).map((c) => {
          const m = c.match(/\$?([\d.]+)/);
          return m ? parseFloat(m[1]) : null;
        }).filter((p) => p !== null);

        if (prices.length < 3 || !modelName || modelName.length < 3) continue;
        const slug = normalizeModelName(modelName);
        const cat = ["powerful", "versatile", "lightweight"].includes(category?.toLowerCase())
          ? category.toLowerCase() : null;

        if (vendor.hasCacheWrite && prices.length >= 4) {
          models[slug] = { input: prices[0], cachedInput: prices[1], cacheWrite: prices[2], output: prices[3], vendor: vendor.name, category: cat };
        } else {
          models[slug] = { input: prices[0], cachedInput: prices[1], output: prices[2], vendor: vendor.name, category: cat };
        }
      }
    } else {
      // Fallback: text-based parsing (simplified markdown)
      const pricePattern = /\$(\d+\.?\d*)/g;
      for (const line of vendorSection.split("\n")) {
        const prices = [];
        let match;
        while ((match = pricePattern.exec(line)) !== null) prices.push(parseFloat(match[1]));
        pricePattern.lastIndex = 0;
        if (prices.length < 3) continue;

        const statusWords = ["GA", "Public preview", "Preview", "Beta"];
        let mn = line;
        for (const sw of statusWords) { const idx = mn.indexOf(sw); if (idx > 0) { mn = mn.slice(0, idx); break; } }
        mn = mn.replace(/\[.*?\]/g, "").replace(/\d+$/, "").trim();
        if (!mn || mn.length < 3) continue;

        const slug = normalizeModelName(mn);
        const categories = ["Powerful", "Versatile", "Lightweight"];
        let cat = null;
        for (const c of categories) { if (line.includes(c)) { cat = c.toLowerCase(); break; } }

        if (vendor.hasCacheWrite && prices.length >= 4) {
          models[slug] = { input: prices[0], cachedInput: prices[1], cacheWrite: prices[2], output: prices[3], vendor: vendor.name, category: cat };
        } else {
          models[slug] = { input: prices[0], cachedInput: prices[1], output: prices[2], vendor: vendor.name, category: cat };
        }
      }
    }
  }

  return Object.keys(models).length > 0 ? models : null;
}

/**
 * Fetch live pricing from GitHub docs. Returns parsed models or null on failure.
 * Results are cached for 1 hour.
 */
export async function fetchLivePricing() {
  const now = Date.now();
  if (livePricingCache && (now - livePricingFetchedAt) < CACHE_TTL_MS) {
    return livePricingCache;
  }

  try {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), 10000);

    const resp = await fetch(PRICING_URL, {
      signal: controller.signal,
      headers: { "Accept": "text/html", "User-Agent": "copilot-insights/1.0" },
    });
    clearTimeout(timeout);

    if (!resp.ok) return null;
    const html = await resp.text();

    const parsed = parsePricingFromPage(html);
    if (parsed && Object.keys(parsed).length >= 5) {
      livePricingCache = {
        models: parsed,
        fetchedAt: new Date().toISOString(),
        source: PRICING_URL,
        modelCount: Object.keys(parsed).length,
      };
      livePricingFetchedAt = now;
      return livePricingCache;
    }
  } catch { /* fetch failed — use fallback */ }
  return null;
}

/**
 * Get the current effective pricing table.
 * Returns live pricing if available, otherwise the hardcoded fallback.
 */
export function getLivePricingOrFallback() {
  if (livePricingCache) {
    return {
      models: livePricingCache.models,
      source: "live",
      fetchedAt: livePricingCache.fetchedAt,
      url: PRICING_URL,
    };
  }
  return {
    models: MODEL_PRICING,
    source: "hardcoded",
    fetchedAt: null,
    url: PRICING_URL,
  };
}

/**
 * Get pricing for a model, with fallback for unknown models.
 * "auto" resolves to default pricing since the actual model isn't known.
 */
export function getModelPricing(model) {
  if (!model || model === "auto" || model === "unknown") return DEFAULT_PRICING;
  // Normalize: lowercase, trim
  const normalized = model.toLowerCase().trim();
  if (MODEL_PRICING[normalized]) return MODEL_PRICING[normalized];
  // Prefix match (e.g., "claude-sonnet-4.6-xxx" matches "claude-sonnet-4.6")
  const key = Object.keys(MODEL_PRICING).find((k) => normalized.startsWith(k));
  return key ? MODEL_PRICING[key] : DEFAULT_PRICING;
}

/**
 * Estimate cost in USD from token counts and model.
 */
export function estimateCost(inputTokens, outputTokens, model, cachedInputTokens = 0) {
  const pricing = getModelPricing(model);
  const freshInput = inputTokens - cachedInputTokens;
  return (
    (freshInput / 1_000_000) * pricing.input +
    (cachedInputTokens / 1_000_000) * pricing.cachedInput +
    (outputTokens / 1_000_000) * pricing.output
  );
}

// ── Real Token Data (events table) ──────────────────────────

/**
 * Get real token usage from the events table for a session.
 * Returns null if events table is unavailable.
 */
export function getRealTokenUsage(sessionId) {
  if (!hasTable("events")) return null;
  const db = getDb();
  try {
    return db
      .prepare(
        `SELECT usage_model, usage_input_tokens, usage_output_tokens,
                usage_cache_read_tokens, usage_cache_write_tokens,
                usage_cost, usage_duration
         FROM events
         WHERE session_id = ? AND usage_input_tokens IS NOT NULL
         ORDER BY rowid`
      )
      .all(sessionId);
  } catch {
    return null;
  }
}

/**
 * Get the model used in a session from the events table.
 */
export function getSessionModel(sessionId) {
  if (!hasTable("events")) return null;
  const db = getDb();
  try {
    const row = db
      .prepare(
        `SELECT usage_model FROM events
         WHERE session_id = ? AND usage_model IS NOT NULL
         LIMIT 1`
      )
      .get(sessionId);
    return row?.usage_model || null;
  } catch {
    return null;
  }
}

// ── Per-Session Token Analysis ──────────────────────────────

/**
 * Analyze token usage for a single session.
 * Priority: 1) JSONL real data, 2) Events table, 3) Text estimation.
 */
function analyzeSessionTokensWithTurns(sessionId, turns, { includePerTurn = true, preferJsonl = true } = {}) {
  if (!turns || turns.length === 0) return null;

  // Try JSONL first (best source — real output token counts)
  const jsonlData = preferJsonl ? readSessionTokensFromJsonl(sessionId) : null;

  // Then try events table
  const realUsage = !jsonlData ? getRealTokenUsage(sessionId) : null;
  const model = jsonlData?.model || getSessionModel(sessionId);

  // Determine data source
  const hasJsonl = jsonlData && jsonlData.turns.length > 0;
  const hasEvents = !hasJsonl && realUsage && realUsage.length > 0;
  const isEstimated = !hasJsonl && !hasEvents;
  const source = hasJsonl ? "jsonl" : hasEvents ? "events" : "estimated";

  let totalInput = 0;
  let totalOutput = 0;
  let totalCachedInput = 0;
  const perTurn = includePerTurn ? [] : null;
  let productiveTurnIndex = 0;

  if (hasJsonl) {
    // Use JSONL real output tokens + estimate input from text
    totalOutput = jsonlData.totals.outputTokens;
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const inputEst = estimateTokens(turn.user_message);
      totalInput += inputEst;
      const jsonlTurn = jsonlData.turns[productiveTurnIndex];
      const outputReal = jsonlTurn?.outputTokens || estimateTokens(turn.assistant_response);
      if (includePerTurn) {
        perTurn.push({
          turnIndex: turn.turn_index,
          inputTokens: inputEst,
          outputTokens: outputReal,
          total: inputEst + outputReal,
          estimated: !jsonlTurn,
          preview: (turn.user_message || "").slice(0, 80),
        });
      }
      productiveTurnIndex += 1;
    }
  } else if (hasEvents) {
    // Use real token data from events table
    for (const evt of realUsage) {
      totalInput += evt.usage_input_tokens || 0;
      totalOutput += evt.usage_output_tokens || 0;
      totalCachedInput += evt.usage_cache_read_tokens || 0;
    }
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const inputEst = estimateTokens(turn.user_message);
      const outputEst = estimateTokens(turn.assistant_response);
      if (includePerTurn) {
        perTurn.push({
          turnIndex: turn.turn_index,
          inputTokens: inputEst,
          outputTokens: outputEst,
          total: inputEst + outputEst,
          estimated: true,
          preview: (turn.user_message || "").slice(0, 80),
        });
      }
    }
  } else {
    // Estimate from text
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const inputEst = estimateTokens(turn.user_message);
      const outputEst = estimateTokens(turn.assistant_response);
      totalInput += inputEst;
      totalOutput += outputEst;
      if (includePerTurn) {
        perTurn.push({
          turnIndex: turn.turn_index,
          inputTokens: inputEst,
          outputTokens: outputEst,
          total: inputEst + outputEst,
          estimated: true,
          preview: (turn.user_message || "").slice(0, 80),
        });
      }
    }
  }

  const total = totalInput + totalOutput;
  const cost = estimateCost(totalInput, totalOutput, model, totalCachedInput);
  const turnCount = includePerTurn
    ? perTurn.length
    : turns.reduce((count, turn) => count + (isAutoGenerated(turn.user_message) ? 0 : 1), 0);

  const result = {
    sessionId,
    model: model || "unknown",
    isEstimated,
    source,
    tokens: { input: totalInput, output: totalOutput, cached: totalCachedInput, total },
    estimatedCost: cost,
    perTurn: perTurn || [],
    turnCount,
    avgTokensPerTurn: turnCount > 0 ? Math.round(total / turnCount) : 0,
  };

  return result;
}

export function analyzeSessionTokens(sessionId, { includePerTurn = true, preferJsonl = true } = {}) {
  const cached = getCachedSessionTokenAnalysis(sessionId, { requirePerTurn: includePerTurn });
  if (cached) return cached;

  const turns = getSessionTurns(sessionId);
  const result = analyzeSessionTokensWithTurns(sessionId, turns, { includePerTurn, preferJsonl });
  if (!result) return null;

  setCachedSessionTokenAnalysis(sessionId, result, { hasPerTurn: includePerTurn });
  return result;
}

/**
 * Compute a token efficiency score (0-100) for a session.
 * Factors: output-to-input ratio, cache utilization, tokens per productive turn.
 */
export function computeTokenEfficiencyScore(sessionId) {
  const tokenData = analyzeSessionTokens(sessionId, { includePerTurn: false });
  if (!tokenData) return null;

  // 1. Output-to-input ratio (higher = more efficient AI usage) - 40%
  // Good: output > 2x input. Bad: output < input
  const ratio = tokenData.tokens.output / Math.max(tokenData.tokens.input, 1);
  const ratioScore = Math.min(100, Math.round(ratio / 3 * 100)); // 3x = 100

  // 2. Tokens per productive turn (lower = more efficient) - 40%
  // Good: < 2000 tokens/turn. Bad: > 8000 tokens/turn
  const tokensPerTurn = tokenData.avgTokensPerTurn || 0;
  const turnScore = tokensPerTurn > 0
    ? Math.max(0, Math.min(100, Math.round(100 - (tokensPerTurn - 2000) / 60)))
    : 50;

  // 3. Cache utilization (if available) - 20%
  const cacheRatio = tokenData.tokens.cached > 0
    ? (tokenData.tokens.cached / Math.max(tokenData.tokens.input, 1)) * 100
    : 0;
  const cacheScore = Math.min(100, Math.round(cacheRatio * 2)); // 50% cache = 100

  return {
    score: Math.round(ratioScore * 0.4 + turnScore * 0.4 + cacheScore * 0.2),
    components: { ratioScore, turnScore, cacheScore },
    metrics: {
      outputInputRatio: Math.round(ratio * 100) / 100,
      tokensPerTurn,
      cacheRatio: Math.round(cacheRatio),
      totalCost: tokenData.estimatedCost,
      model: tokenData.model,
    },
  };
}

// ── Batch Token Analysis ────────────────────────────────────

export function createEmptyTokenBatchAnalysis() {
  return {
    sessionsAnalyzed: 0,
    isEstimated: true,
    totals: { input: 0, output: 0, cached: 0, total: 0 },
    estimatedCost: 0,
    avgTokensPerSession: 0,
    avgCostPerSession: 0,
    byModel: [],
    sessions: [],
    costBreakdown: { input: 0, output: 0 },
  };
}

export function createTokenBatchAccumulator(totalSessions = 0) {
  return {
    totalSessions,
    totalInput: 0,
    totalOutput: 0,
    totalCached: 0,
    totalCost: 0,
    totalInputCost: 0,
    totalOutputCost: 0,
    anyEstimated: false,
    byModel: {},
    sessionResults: [],
  };
}

function addTokenBatchResult(accumulator, session, result) {
  if (!result) return;

  if (result.isEstimated) accumulator.anyEstimated = true;
  accumulator.totalInput += result.tokens.input;
  accumulator.totalOutput += result.tokens.output;
  accumulator.totalCached += result.tokens.cached;
  accumulator.totalCost += result.estimatedCost;

  const pricing = getModelPricing(result.model);
  const freshIn = result.tokens.input - result.tokens.cached;
  accumulator.totalInputCost +=
    (freshIn / 1_000_000) * pricing.input +
    (result.tokens.cached / 1_000_000) * pricing.cachedInput;
  accumulator.totalOutputCost += (result.tokens.output / 1_000_000) * pricing.output;

  const model = result.model || "unknown";
  if (!accumulator.byModel[model]) {
    accumulator.byModel[model] = {
      model,
      sessions: 0,
      input: 0,
      output: 0,
      cached: 0,
      total: 0,
      cost: 0,
    };
  }

  accumulator.byModel[model].sessions += 1;
  accumulator.byModel[model].input += result.tokens.input;
  accumulator.byModel[model].output += result.tokens.output;
  accumulator.byModel[model].cached += result.tokens.cached;
  accumulator.byModel[model].total += result.tokens.total;
  accumulator.byModel[model].cost += result.estimatedCost;

  accumulator.sessionResults.push({
    sessionId: session.id,
    repository: session.repository,
    branch: session.branch,
    summary: session.summary,
    createdAt: session.created_at,
    model: result.model,
    tokens: result.tokens,
    estimatedCost: result.estimatedCost,
    turnCount: result.turnCount,
    avgTokensPerTurn: result.avgTokensPerTurn,
  });
}

export function analyzeTokenSessionsChunk(sessions, accumulator = createTokenBatchAccumulator()) {
  if (!sessions || sessions.length === 0) return accumulator;

  const uncachedSessionIds = sessions
    .filter((session) => !getCachedSessionTokenAnalysis(session.id))
    .map((session) => session.id);
  const turnsBySession = batchGetSessionTurns(uncachedSessionIds);

  for (const session of sessions) {
    let result = getCachedSessionTokenAnalysis(session.id);
    if (!result) {
      const turns = turnsBySession.get(session.id) || [];
      result = analyzeSessionTokensWithTurns(session.id, turns, { includePerTurn: false });
      if (result) {
        setCachedSessionTokenAnalysis(session.id, result, { hasPerTurn: false });
      }
    }
    addTokenBatchResult(accumulator, session, result);
  }

  return accumulator;
}

export function finalizeTokenBatchAccumulator(accumulator) {
  if (!accumulator || accumulator.sessionResults.length === 0) {
    return createEmptyTokenBatchAnalysis();
  }

  const total = accumulator.totalInput + accumulator.totalOutput;
  const analyzed = accumulator.sessionResults.length;
  return {
    sessionsAnalyzed: analyzed,
    isEstimated: accumulator.anyEstimated,
    totals: {
      input: accumulator.totalInput,
      output: accumulator.totalOutput,
      cached: accumulator.totalCached,
      total,
    },
    estimatedCost: accumulator.totalCost,
    costBreakdown: {
      input: accumulator.totalInputCost,
      output: accumulator.totalOutputCost,
    },
    avgTokensPerSession: analyzed > 0 ? Math.round(total / analyzed) : 0,
    avgCostPerSession: analyzed > 0 ? accumulator.totalCost / analyzed : 0,
    byModel: Object.values(accumulator.byModel).sort((a, b) => b.total - a.total),
    sessions: accumulator.sessionResults.sort((a, b) => b.tokens.total - a.tokens.total),
  };
}

/**
 * Analyze token usage across multiple sessions.
 */
export function analyzeTokensBatch({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  if (sessions.length === 0) return createEmptyTokenBatchAnalysis();
  const accumulator = createTokenBatchAccumulator(sessions.length);
  analyzeTokenSessionsChunk(sessions, accumulator);
  return finalizeTokenBatchAccumulator(accumulator);
}
