// Token usage estimation and cost analysis.
// Reads real token data from JSONL session-state files when available,
// falls back to text-based estimation, and auto-upgrades to real data
// from the events table if Copilot CLI ever stores it locally.

import { readFileSync, readdirSync, existsSync } from "node:fs";
import { join } from "node:path";
import { homedir } from "node:os";
import { getDb, hasTable, listSessions, getSessionTurns } from "./db.mjs";
import { isAutoGenerated } from "./autogenerated.mjs";

// ── JSONL Session-State Reader ──────────────────────────────
// Copilot CLI writes events.jsonl files per session with real token data.

const SESSION_STATE_PATH =
  process.env.COPILOT_SESSION_STATE_PATH ||
  join(homedir(), ".copilot", "session-state");

function parseJsonlFile(filePath) {
  try {
    const content = readFileSync(filePath, "utf-8");
    const results = [];
    for (const line of content.split("\n")) {
      const trimmed = line.trim();
      if (!trimmed) continue;
      try { results.push(JSON.parse(trimmed)); } catch { /* skip malformed */ }
    }
    return results;
  } catch { return []; }
}

/**
 * Read real token usage from JSONL session-state files.
 * Returns null if no usable data found.
 */
function readSessionTokensFromJsonl(sessionId) {
  if (!sessionId) return null;
  const sessionDir = join(SESSION_STATE_PATH, sessionId);
  if (!existsSync(sessionDir)) return null;

  let jsonlFiles;
  try {
    const all = readdirSync(sessionDir);
    const wellKnown = ["events.jsonl", "turns.jsonl"];
    const preferred = wellKnown.filter((f) => all.includes(f));
    const rest = all.filter((f) => f.endsWith(".jsonl") && !preferred.includes(f));
    jsonlFiles = [...preferred, ...rest];
  } catch { return null; }

  if (jsonlFiles.length === 0) return null;

  const assistantMessages = [];
  let sessionModel = null;

  for (const file of jsonlFiles) {
    const objects = parseJsonlFile(join(sessionDir, file));
    for (const obj of objects) {
      // Extract model from session events
      if (obj.type === "session.model_change" && obj.data?.newModel) {
        sessionModel = obj.data.newModel;
      }
      // Extract token data from assistant messages
      if (obj.type === "assistant.message" && typeof obj.data?.outputTokens === "number") {
        assistantMessages.push({
          turnIndex: assistantMessages.length,
          outputTokens: obj.data.outputTokens,
          model: sessionModel,
        });
      }
    }
  }

  if (assistantMessages.length === 0) return null;

  let totalOutput = 0;
  for (const m of assistantMessages) totalOutput += m.outputTokens;

  return {
    source: "jsonl",
    model: sessionModel,
    turns: assistantMessages,
    totals: { outputTokens: totalOutput },
  };
}

// ── Token Estimation ─────────────────────────────────────────
// Standard LLM approximation: ~4 characters per token for English text.
// ~75% accurate — clearly labeled as "estimated" in all outputs.

const CHARS_PER_TOKEN = 4;

/**
 * Estimate token count from text.
 * @param {string} text
 * @returns {number}
 */
export function estimateTokens(text) {
  if (!text) return 0;
  return Math.ceil(text.length / CHARS_PER_TOKEN);
}

// ── Model Pricing ────────────────────────────────────────────
// Prices per 1M tokens (USD). Based on publicly listed API pricing.
// These are approximate and may change — used for directional estimates only.

