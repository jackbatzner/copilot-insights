// ── JSONL Session-State Reader ──────────────────────────────
// Copilot CLI writes events.jsonl files per session with real token data.

const SESSION_TOKEN_CACHE_TTL_MS = 60_000;
const sessionTokenCache = new Map();

function getCachedSessionTokenAnalysis(sessionId, { requirePerTurn = false } = {}) {
  const entry = sessionTokenCache.get(sessionId);
  if (!entry) return null;
  if (Date.now() - entry.ts > SESSION_TOKEN_CACHE_TTL_MS) {
    sessionTokenCache.delete(sessionId);
    return null;
  }
  if (requirePerTurn && !entry.hasPerTurn) return null;
  return entry.value;
}

function setCachedSessionTokenAnalysis(sessionId, value, { hasPerTurn = true } = {}) {
  sessionTokenCache.set(sessionId, { value, ts: Date.now(), hasPerTurn });
}

/**
 * Read real token usage from JSONL session-state files.
 * Returns null if no usable data found.
 */
function readSessionTokensFromJsonl(sessionId) {
  if (!sessionId) return null;
  const jsonlFiles = listSessionJsonlFiles(sessionId);

  if (jsonlFiles.length === 0) return null;

  const assistantMessages = [];
  let sessionModel = null;

  for (const file of jsonlFiles) {
    scanJsonlFile(
      file,
      (line) =>
        line.includes("\"session.model_change\"") ||
        (line.includes("\"assistant.message\"") && line.includes("\"outputTokens\"")),
      (obj) => {
        // Extract model from session events
        if (obj.type === "session.model_change" && obj.data?.newModel) {
          sessionModel = obj.data.newModel;
        }
        // Extract token data from assistant messages
        if (obj.type === "assistant.message" && typeof obj.data?.outputTokens === "number") {
          assistantMessages.push({
            turnIndex: assistantMessages.length,
            outputTokens: obj.data.outputTokens,
            model: sessionModel,
          });
        }
      }
    );
  }

  if (assistantMessages.length === 0) return null;

  let totalOutput = 0;
  for (const m of assistantMessages) totalOutput += m.outputTokens;

  return {
    source: "jsonl",
    model: sessionModel,
    turns: assistantMessages,
    totals: { outputTokens: totalOutput },
  };
}

// ── Token Estimation ─────────────────────────────────────────
// Standard LLM approximation: ~4 characters per token for English text.
// ~75% accurate — clearly labeled as "estimated" in all outputs.

const CHARS_PER_TOKEN = 4;

/**
 * Estimate token count from text.
 * @param {string} text
 * @returns {number}
 */
export function estimateTokens(text) {
  if (!text) return 0;
  return Math.ceil(text.length / CHARS_PER_TOKEN);
}

