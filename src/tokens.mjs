// Token usage estimation and cost analysis.
// Estimates tokens from turn text when the events table is unavailable,
// with automatic upgrade to real data when Copilot CLI stores events locally.

import { getDb, hasTable, listSessions, getSessionTurns } from "./db.mjs";
import { isAutoGenerated } from "./autogenerated.mjs";

// ── Token Estimation ─────────────────────────────────────────
// Standard LLM approximation: ~4 characters per token for English text.
// ~75% accurate — clearly labeled as "estimated" in all outputs.

const CHARS_PER_TOKEN = 4;

/**
 * Estimate token count from text.
 * @param {string} text
 * @returns {number}
 */
export function estimateTokens(text) {
  if (!text) return 0;
  return Math.ceil(text.length / CHARS_PER_TOKEN);
}

// ── Model Pricing ────────────────────────────────────────────
// Prices per 1M tokens (USD). Based on publicly listed API pricing.
// These are approximate and may change — used for directional estimates only.

export const MODEL_PRICING = {
  // Claude models
  "claude-opus-4.7":       { input: 15.00, output: 75.00, cachedInput: 1.50 },
  "claude-opus-4.6":       { input: 15.00, output: 75.00, cachedInput: 1.50 },
  "claude-opus-4.5":       { input: 15.00, output: 75.00, cachedInput: 1.50 },
  "claude-sonnet-4.6":     { input: 3.00,  output: 15.00, cachedInput: 0.30 },
  "claude-sonnet-4.5":     { input: 3.00,  output: 15.00, cachedInput: 0.30 },
  "claude-sonnet-4":       { input: 3.00,  output: 15.00, cachedInput: 0.30 },
  "claude-haiku-4.5":      { input: 0.80,  output: 4.00,  cachedInput: 0.08 },
  // GPT models
  "gpt-5.4":               { input: 2.50,  output: 10.00, cachedInput: 0.63 },
  "gpt-5.4-mini":          { input: 0.40,  output: 1.60,  cachedInput: 0.10 },
  "gpt-5.3-codex":         { input: 2.50,  output: 10.00, cachedInput: 0.63 },
  "gpt-5.2-codex":         { input: 2.50,  output: 10.00, cachedInput: 0.63 },
  "gpt-5.2":               { input: 2.50,  output: 10.00, cachedInput: 0.63 },
  "gpt-5-mini":            { input: 0.40,  output: 1.60,  cachedInput: 0.10 },
  "gpt-4.1":               { input: 2.00,  output: 8.00,  cachedInput: 0.50 },
};

// Fallback pricing for unknown models (mid-tier estimate)
const DEFAULT_PRICING = { input: 3.00, output: 15.00, cachedInput: 0.30 };

/**
 * Get pricing for a model, with fallback for unknown models.
 */
export function getModelPricing(model) {
  if (!model) return DEFAULT_PRICING;
  // Try exact match, then prefix match (e.g., "claude-sonnet-4.6" matches "claude-sonnet-4.6")
  if (MODEL_PRICING[model]) return MODEL_PRICING[model];
  const key = Object.keys(MODEL_PRICING).find((k) => model.startsWith(k));
  return key ? MODEL_PRICING[key] : DEFAULT_PRICING;
}

/**
 * Estimate cost in USD from token counts and model.
 */
export function estimateCost(inputTokens, outputTokens, model, cachedInputTokens = 0) {
  const pricing = getModelPricing(model);
  const freshInput = inputTokens - cachedInputTokens;
  return (
    (freshInput / 1_000_000) * pricing.input +
    (cachedInputTokens / 1_000_000) * pricing.cachedInput +
    (outputTokens / 1_000_000) * pricing.output
  );
}

// ── Real Token Data (events table) ──────────────────────────

/**
 * Get real token usage from the events table for a session.
 * Returns null if events table is unavailable.
 */
export function getRealTokenUsage(sessionId) {
  if (!hasTable("events")) return null;
  const db = getDb();
  try {
    return db
      .prepare(
        `SELECT usage_model, usage_input_tokens, usage_output_tokens,
                usage_cache_read_tokens, usage_cache_write_tokens,
                usage_cost, usage_duration
         FROM events
         WHERE session_id = ? AND usage_input_tokens IS NOT NULL
         ORDER BY rowid`
      )
      .all(sessionId);
  } catch {
    return null;
  }
}

/**
 * Get the model used in a session from the events table.
 */
export function getSessionModel(sessionId) {
  if (!hasTable("events")) return null;
  const db = getDb();
  try {
    const row = db
      .prepare(
        `SELECT usage_model FROM events
         WHERE session_id = ? AND usage_model IS NOT NULL
         LIMIT 1`
      )
      .get(sessionId);
    return row?.usage_model || null;
  } catch {
    return null;
  }
}

// ── Per-Session Token Analysis ──────────────────────────────

/**
 * Analyze token usage for a single session.
 * Uses real data from events table if available, otherwise estimates from text.
 */
export function analyzeSessionTokens(sessionId) {
  const turns = getSessionTurns(sessionId);
  if (!turns || turns.length === 0) return null;

  const realUsage = getRealTokenUsage(sessionId);
  const model = getSessionModel(sessionId);
  const isEstimated = !realUsage || realUsage.length === 0;

  let totalInput = 0;
  let totalOutput = 0;
  let totalCachedInput = 0;
  const perTurn = [];

  if (!isEstimated) {
    // Use real token data — aggregate from events
    for (const evt of realUsage) {
      totalInput += evt.usage_input_tokens || 0;
      totalOutput += evt.usage_output_tokens || 0;
      totalCachedInput += evt.usage_cache_read_tokens || 0;
    }
    // Map turns for display — real data doesn't map 1:1 to turns,
    // so we still estimate per-turn for the breakdown view
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const inputEst = estimateTokens(turn.user_message);
      const outputEst = estimateTokens(turn.assistant_response);
      perTurn.push({
        turnIndex: turn.turn_index,
        inputTokens: inputEst,
        outputTokens: outputEst,
        total: inputEst + outputEst,
        estimated: true,
      });
    }
  } else {
    // Estimate from text
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const inputEst = estimateTokens(turn.user_message);
      const outputEst = estimateTokens(turn.assistant_response);
      totalInput += inputEst;
      totalOutput += outputEst;
      perTurn.push({
        turnIndex: turn.turn_index,
        inputTokens: inputEst,
        outputTokens: outputEst,
        total: inputEst + outputEst,
        estimated: true,
      });
    }
  }

  const total = totalInput + totalOutput;
  const cost = estimateCost(totalInput, totalOutput, model, totalCachedInput);

  return {
    sessionId,
    model: model || "unknown",
    isEstimated,
    tokens: { input: totalInput, output: totalOutput, cached: totalCachedInput, total },
    estimatedCost: cost,
    perTurn,
    turnCount: perTurn.length,
    avgTokensPerTurn: perTurn.length > 0 ? Math.round(total / perTurn.length) : 0,
  };
}

// ── Batch Token Analysis ────────────────────────────────────

/**
 * Analyze token usage across multiple sessions.
 */
export function analyzeTokensBatch({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  if (sessions.length === 0) {
    return {
      sessionsAnalyzed: 0,
      isEstimated: true,
      totals: { input: 0, output: 0, cached: 0, total: 0 },
      estimatedCost: 0,
      avgTokensPerSession: 0,
      avgCostPerSession: 0,
      byModel: {},
      sessions: [],
    };
  }

  let totalInput = 0;
  let totalOutput = 0;
  let totalCached = 0;
  let totalCost = 0;
  let anyEstimated = false;
  const byModel = {};
  const sessionResults = [];

  for (const s of sessions) {
    const result = analyzeSessionTokens(s.id);
    if (!result) continue;

    if (result.isEstimated) anyEstimated = true;
    totalInput += result.tokens.input;
    totalOutput += result.tokens.output;
    totalCached += result.tokens.cached;
    totalCost += result.estimatedCost;

    const m = result.model || "unknown";
    if (!byModel[m]) {
      byModel[m] = { model: m, sessions: 0, input: 0, output: 0, cached: 0, total: 0, cost: 0 };
    }
    byModel[m].sessions += 1;
    byModel[m].input += result.tokens.input;
    byModel[m].output += result.tokens.output;
    byModel[m].cached += result.tokens.cached;
    byModel[m].total += result.tokens.total;
    byModel[m].cost += result.estimatedCost;

    sessionResults.push({
      sessionId: s.id,
      repository: s.repository,
      branch: s.branch,
      summary: s.summary,
      createdAt: s.created_at,
      model: result.model,
      tokens: result.tokens,
      estimatedCost: result.estimatedCost,
      turnCount: result.turnCount,
      avgTokensPerTurn: result.avgTokensPerTurn,
    });
  }

  const total = totalInput + totalOutput;
  const analyzed = sessionResults.length;

  return {
    sessionsAnalyzed: analyzed,
    isEstimated: anyEstimated,
    totals: { input: totalInput, output: totalOutput, cached: totalCached, total },
    estimatedCost: totalCost,
    avgTokensPerSession: analyzed > 0 ? Math.round(total / analyzed) : 0,
    avgCostPerSession: analyzed > 0 ? totalCost / analyzed : 0,
    byModel: Object.values(byModel).sort((a, b) => b.total - a.total),
    sessions: sessionResults.sort((a, b) => b.tokens.total - a.tokens.total),
  };
}
