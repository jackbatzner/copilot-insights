/**
 * Break down token usage by model.
 * Wraps analyzeTokensBatch().byModel with extra stats.
 */
export function tokensByModel({ repo, since, excludeIds } = {}) {
  const batch = analyzeTokensBatch({ repo, since, excludeIds });
  return {
    isEstimated: batch.isEstimated,
    models: batch.byModel.map((m) => ({
      ...m,
      avgTokensPerSession: m.sessions > 0 ? Math.round(m.total / m.sessions) : 0,
      avgCostPerSession: m.sessions > 0 ? m.cost / m.sessions : 0,
      inputOutputRatio: m.output > 0 ? Math.round((m.input / m.output) * 100) / 100 : 0,
      cacheHitRate: m.input > 0 ? Math.round((m.cached / m.input) * 100) : 0,
    })),
    totals: batch.totals,
    totalCost: batch.estimatedCost,
  };
}

// ── Tokens Per Redirection ──────────────────────────────────

/**
 * Correlate redirections with token cost.
 * Shows how much each redirection costs in tokens.
 */
export function tokensPerRedirection({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  let totalRedirectionTokens = 0;
  let totalProductiveTokens = 0;
  let redirectionCount = 0;
  let productiveCount = 0;
  const byCategory = {};

  for (const s of sessions) {
    const turns = getSessionTurns(s.id);
    if (!turns || turns.length < 2) continue;

    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const cleaned = (turn.user_message || "")
        .replace(/<[^>]*>[\s\S]*?<\/[^>]*>/gi, "")
        .replace(/<[^>]+>/g, " ")
        .trim();
      if (!cleaned || cleaned.length < 3) continue;

      const turnTokens = estimateTokens(turn.user_message) + estimateTokens(turn.assistant_response);
      const matches = matchPatterns(cleaned);

      if (matches.length > 0) {
        totalRedirectionTokens += turnTokens;
        redirectionCount++;
        for (const m of matches) {
          if (!byCategory[m.category]) byCategory[m.category] = { tokens: 0, count: 0 };
          byCategory[m.category].tokens += turnTokens;
          byCategory[m.category].count++;
        }
      } else {
        totalProductiveTokens += turnTokens;
        productiveCount++;
      }
    }
  }

  const totalTokens = totalRedirectionTokens + totalProductiveTokens;
  const wasteRate = totalTokens > 0 ? Math.round((totalRedirectionTokens / totalTokens) * 100) : 0;

  return {
    sessionsAnalyzed: sessions.length,
    redirectionTokens: totalRedirectionTokens,
    productiveTokens: totalProductiveTokens,
    totalTokens,
    wasteRate,
    avgTokensPerRedirection: redirectionCount > 0 ? Math.round(totalRedirectionTokens / redirectionCount) : 0,
    avgTokensPerProductiveTurn: productiveCount > 0 ? Math.round(totalProductiveTokens / productiveCount) : 0,
    redirectionCount,
    productiveCount,
    byCategory: Object.entries(byCategory)
      .map(([cat, data]) => ({
        category: cat,
        tokens: data.tokens,
        count: data.count,
        avgTokens: Math.round(data.tokens / data.count),
      }))
      .sort((a, b) => b.tokens - a.tokens),
  };
}

// ── Tokens by Work Style ────────────────────────────────────

const PLAN_WORDS = /\b(plan|approach|design|architecture|strategy|think about|consider|before we|let's figure|outline|breakdown)\b/i;

/**
 * Compare token costs across work styles (structured, vibe, iterative, mixed).
 */
export function tokensByWorkStyle({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  const styles = { structured: { tokens: 0, cost: 0, sessions: 0, redirections: 0 },
                   iterative:  { tokens: 0, cost: 0, sessions: 0, redirections: 0 },
                   vibe:       { tokens: 0, cost: 0, sessions: 0, redirections: 0 },
                   mixed:      { tokens: 0, cost: 0, sessions: 0, redirections: 0 } };

  for (const s of sessions) {
    const turns = getSessionTurns(s.id);
    if (!turns || turns.length < 2) continue;
    const files = getSessionFiles(s.id);

    // Quick style classification (mirrors work-style.mjs logic)
    const firstFileTurn = files.length > 0 ? Math.min(...files.map((f) => f.turn_index)) : turns.length;
    const planTurns = turns.filter((t) => {
      const c = (t.user_message || "").replace(/<[^>]*>[\s\S]*?<\/[^>]*>/gi, "").trim();
      return PLAN_WORDS.test(c);
    }).length;

    let style;
    if (firstFileTurn >= 3 && planTurns >= 2) style = "structured";
    else if (firstFileTurn <= 1) style = "vibe";
    else if (firstFileTurn >= 2) style = "iterative";
    else style = "mixed";

    const tokenResult = analyzeSessionTokens(s.id);
    if (!tokenResult) continue;

    // Count redirections
    let redirs = 0;
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const cleaned = (turn.user_message || "").replace(/<[^>]*>[\s\S]*?<\/[^>]*>/gi, "").replace(/<[^>]+>/g, " ").trim();
      if (cleaned.length >= 3 && matchPatterns(cleaned).length > 0) redirs++;
    }

    styles[style].tokens += tokenResult.tokens.total;
    styles[style].cost += tokenResult.estimatedCost;
    styles[style].sessions += 1;
    styles[style].redirections += redirs;
  }

  const result = Object.entries(styles).map(([style, data]) => ({
    style,
    emoji: { structured: "🎯", iterative: "🔄", vibe: "🌊", mixed: "⚡" }[style],
    sessions: data.sessions,
    totalTokens: data.tokens,
    totalCost: data.cost,
    avgTokensPerSession: data.sessions > 0 ? Math.round(data.tokens / data.sessions) : 0,
    avgCostPerSession: data.sessions > 0 ? data.cost / data.sessions : 0,
    avgRedirectionsPerSession: data.sessions > 0 ? Math.round((data.redirections / data.sessions) * 10) / 10 : 0,
  }));

  // Find cheapest style
  const withData = result.filter((r) => r.sessions > 0);
  const cheapest = withData.length > 0 ? withData.reduce((a, b) => a.avgCostPerSession < b.avgCostPerSession ? a : b) : null;
  const mostExpensive = withData.length > 0 ? withData.reduce((a, b) => a.avgCostPerSession > b.avgCostPerSession ? a : b) : null;

  let insight = null;
  if (cheapest && mostExpensive && cheapest.style !== mostExpensive.style) {
    const savings = Math.round(((mostExpensive.avgCostPerSession - cheapest.avgCostPerSession) / mostExpensive.avgCostPerSession) * 100);
    if (savings > 10) {
      insight = `${cheapest.emoji} ${cheapest.style} sessions cost ~${savings}% less than ${mostExpensive.emoji} ${mostExpensive.style} sessions on average.`;
    }
  }

  return { styles: result, insight, cheapestStyle: cheapest?.style, mostExpensiveStyle: mostExpensive?.style };
}

// ── Token Efficiency Metrics ────────────────────────────────

/**
 * Compute token efficiency: tokens per file operation, tokens per productive turn, ROI.
 */
export function tokenEfficiencyMetrics({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  let totalTokens = 0;
  let totalFileOps = 0;
  let totalProductiveTurns = 0;
  let totalRedirectionTurns = 0;
  let totalCreates = 0;
  let totalEdits = 0;

  for (const s of sessions) {
    const turns = getSessionTurns(s.id);
    if (!turns || turns.length < 2) continue;
    const files = getSessionFiles(s.id);

    let sessionTokens = 0;
    for (const turn of turns) {
      if (isAutoGenerated(turn.user_message)) continue;
      const turnTokens = estimateTokens(turn.user_message) + estimateTokens(turn.assistant_response);
      sessionTokens += turnTokens;
      const cleaned = (turn.user_message || "").replace(/<[^>]*>[\s\S]*?<\/[^>]*>/gi, "").replace(/<[^>]+>/g, " ").trim();
      if (cleaned.length >= 3 && matchPatterns(cleaned).length > 0) {
        totalRedirectionTurns++;
      } else {
        totalProductiveTurns++;
      }
    }

    totalTokens += sessionTokens;
    totalFileOps += files.length;
    totalCreates += files.filter((f) => f.tool_name === "create").length;
    totalEdits += files.filter((f) => f.tool_name === "edit").length;
  }

  const tokensPerFileOp = totalFileOps > 0 ? Math.round(totalTokens / totalFileOps) : 0;
  const tokensPerProductiveTurn = totalProductiveTurns > 0 ? Math.round(totalTokens / totalProductiveTurns) : 0;
  const productiveRatio = (totalProductiveTurns + totalRedirectionTurns) > 0
    ? Math.round((totalProductiveTurns / (totalProductiveTurns + totalRedirectionTurns)) * 100)
    : 0;

  // "Token ROI" — file operations per 1K tokens
  const tokenROI = totalTokens > 0 ? Math.round((totalFileOps / (totalTokens / 1000)) * 100) / 100 : 0;

  return {
    sessionsAnalyzed: sessions.length,
    totalTokens,
    totalFileOps,
    totalCreates,
    totalEdits,
    tokensPerFileOp,
    tokensPerProductiveTurn,
    productiveTokenRatio: productiveRatio,
    tokenROI,
    totalProductiveTurns,
    totalRedirectionTurns,
  };
}

// ── Weekly Token Trends ─────────────────────────────────────

/**
 * Compute weekly token usage trends.
 */
export function tokenTrends({ repo, since, excludeIds } = {}) {
  const sessions = listSessions({ repo, since, excludeIds });
  const weekMap = new Map();

  for (const s of sessions) {
    const date = new Date(s.created_at);
    if (isNaN(date.getTime())) continue;

    // ISO week key
    const week = getISOWeek(date);
    if (!weekMap.has(week.key)) {
      weekMap.set(week.key, {
        week: week.key,
        startDate: week.startDate,
        input: 0, output: 0, total: 0, cost: 0, sessions: 0,
      });
    }

    const tokenResult = analyzeSessionTokens(s.id);
    if (!tokenResult) continue;

    const entry = weekMap.get(week.key);
    entry.input += tokenResult.tokens.input;
    entry.output += tokenResult.tokens.output;
    entry.total += tokenResult.tokens.total;
    entry.cost += tokenResult.estimatedCost;
    entry.sessions += 1;
  }

  const weeks = [...weekMap.values()].sort((a, b) => a.week.localeCompare(b.week));

  // Trend direction (compare last 2 weeks)
  let trend = "stable";
  if (weeks.length >= 2) {
    const last = weeks[weeks.length - 1];
    const prev = weeks[weeks.length - 2];
    const diff = last.total - prev.total;
    const pct = prev.total > 0 ? (diff / prev.total) * 100 : 0;
    if (pct > 15) trend = "increasing";
    else if (pct < -15) trend = "decreasing";
  }

  return { weeks, trend, weekCount: weeks.length };
}

function getISOWeek(date) {
  const d = new Date(date);
  d.setHours(0, 0, 0, 0);
  d.setDate(d.getDate() + 3 - ((d.getDay() + 6) % 7));
  const yearStart = new Date(d.getFullYear(), 0, 4);
  const weekNum = Math.ceil(((d - yearStart) / 86400000 + 1) / 7);
  const key = `${d.getFullYear()}-W${String(weekNum).padStart(2, "0")}`;

  // Week start date (Monday)
  const startDate = new Date(d);
  startDate.setDate(startDate.getDate() - ((startDate.getDay() + 6) % 7));
  return { key, startDate: startDate.toISOString().split("T")[0] };
}

// ── Budget Projection ───────────────────────────────────────

/**
 * Project monthly token usage and cost based on recent activity.
 */
export function budgetProjection({ repo, since, excludeIds } = {}) {
  const trends = tokenTrends({ repo, since, excludeIds });
  if (trends.weeks.length === 0) {
    return {
      hasData: false,
      projectedMonthlyTokens: 0,
      projectedMonthlyCost: 0,
      avgWeeklyTokens: 0,
      avgWeeklyCost: 0,
      recentWeeks: [],
    };
  }

  // Use last 4 weeks (or all available) for projection
  const recentWeeks = trends.weeks.slice(-4);
  const avgWeeklyTokens = Math.round(recentWeeks.reduce((s, w) => s + w.total, 0) / recentWeeks.length);
  const avgWeeklyCost = recentWeeks.reduce((s, w) => s + w.cost, 0) / recentWeeks.length;
  const avgWeeklySessions = Math.round(recentWeeks.reduce((s, w) => s + w.sessions, 0) / recentWeeks.length);

  const projectedMonthlyTokens = Math.round(avgWeeklyTokens * 4.33);
  const projectedMonthlyCost = avgWeeklyCost * 4.33;

  // Usage alerts
  const alerts = [];
  if (trends.trend === "increasing") {
    alerts.push({ level: "info", message: "Token usage is trending up — keep an eye on spend." });
  }
  if (projectedMonthlyCost > 50) {
    alerts.push({ level: "warning", message: `Projected monthly cost: $${projectedMonthlyCost.toFixed(2)}. Consider optimizing high-cost sessions.` });
  }

  return {
    hasData: true,
    projectedMonthlyTokens,
    projectedMonthlyCost,
    avgWeeklyTokens,
    avgWeeklyCost,
    avgWeeklySessions,
    trend: trends.trend,
    alerts,
    recentWeeks,
  };
}

