/**
 * Generate model recommendations based on usage patterns.
 */
export function modelRecommendations({ repo, since, excludeIds } = {}) {
  const batch = analyzeTokensBatch({ repo, since, excludeIds });
  const sessions = listSessions({ repo, since, excludeIds });

  // Classify sessions by complexity (turns + file ops)
  let simpleSessions = 0;
  let moderateSessions = 0;
  let complexSessions = 0;

  for (const s of sessions) {
    const turns = getSessionTurns(s.id);
    const files = getSessionFiles(s.id);
    const turnCount = turns?.length || 0;
    const fileCount = files?.length || 0;

    if (turnCount <= 3 && fileCount <= 2) simpleSessions++;
    else if (turnCount <= 10 && fileCount <= 8) moderateSessions++;
    else complexSessions++;
  }

  const totalClassified = simpleSessions + moderateSessions + complexSessions;

  // Build personalized recommendations
  const recommendations = [];

  // Recommendation: use lightweight models for simple sessions
  if (simpleSessions > 0 && totalClassified > 0) {
    const simplePercent = Math.round((simpleSessions / totalClassified) * 100);
    if (simplePercent >= 15) {
      recommendations.push({
        id: "use-lightweight-for-simple",
        icon: "⚡",
        title: "Use lightweight models for simple tasks",
        detail: `${simplePercent}% of your sessions (${simpleSessions}) are simple (≤3 turns, ≤2 files). These don't need expensive models.`,
        models: ["gpt-5-mini", "gpt-5.4-nano", "gemini-3-flash", "grok-code-fast-1"],
        potentialSavings: estimateSavings(batch, simpleSessions, totalClassified, "lightweight"),
      });
    }
  }

  // Recommendation: versatile models for moderate sessions
  if (moderateSessions > 0 && totalClassified > 0) {
    const modPercent = Math.round((moderateSessions / totalClassified) * 100);
    if (modPercent >= 10) {
      recommendations.push({
        id: "versatile-for-moderate",
        icon: "⚖️",
        title: "Versatile models for everyday coding",
        detail: `${modPercent}% of sessions (${moderateSessions}) are moderate complexity. Versatile models give the best quality/cost balance.`,
        models: ["claude-sonnet-4.6", "gpt-5.4", "gpt-4.1", "claude-haiku-4.5"],
        potentialSavings: null,
      });
    }
  }

  // Recommendation: powerful models only for complex sessions
  if (complexSessions > 0 && totalClassified > 0) {
    const complexPercent = Math.round((complexSessions / totalClassified) * 100);
    recommendations.push({
      id: "powerful-for-complex",
      icon: "🧠",
      title: "Reserve powerful models for complex work",
      detail: `${complexPercent}% of sessions (${complexSessions}) are complex (10+ turns, 8+ files). These benefit from powerful models.`,
      models: ["claude-opus-4.7", "gpt-5.5", "gpt-5.3-codex", "gemini-2.5-pro"],
      potentialSavings: null,
    });
  }

  // Check if user is overspending on a single model
  const userModels = batch.byModel.filter((m) => m.model !== "unknown");
  if (userModels.length === 1 && totalClassified > 5) {
    const model = userModels[0].model;
    const pricing = getModelPricing(model);
    const guide = MODEL_GUIDE[model];
    const tier = guide?.tier || (pricing.output >= 14 ? "powerful" : pricing.output >= 5 ? "versatile" : "lightweight");

    if (tier === "powerful") {
      recommendations.push({
        id: "diversify-from-powerful",
        icon: "💡",
        title: `Consider alternatives to ${model}`,
        detail: `You exclusively use ${model} (a powerful model) for all tasks. Simpler sessions could use cheaper models and save significantly.`,
        models: ["claude-sonnet-4.6", "gpt-5.4", "gpt-5-mini"],
        potentialSavings: estimateSavings(batch, simpleSessions + moderateSessions, totalClassified, "versatile"),
      });
    } else if (tier === "versatile" && simpleSessions > totalClassified * 0.3) {
      recommendations.push({
        id: "use-mini-for-simple",
        icon: "💡",
        title: `Try lightweight models for simple tasks`,
        detail: `${simpleSessions} of your sessions are simple. A lightweight model could handle them at a fraction of the cost.`,
        models: ["gpt-5-mini", "gpt-5.4-nano", "grok-code-fast-1"],
        potentialSavings: estimateSavings(batch, simpleSessions, totalClassified, "lightweight"),
      });
    }
  }

  // Build model guide with pricing info
  const guide = Object.entries(MODEL_GUIDE).map(([model, info]) => {
    const pricing = MODEL_PRICING[model] || {};
    return {
      model,
      ...info,
      inputCost: pricing.input ?? null,
      outputCost: pricing.output ?? null,
      cachedInputCost: pricing.cachedInput ?? null,
    };
  });

  return {
    recommendations,
    sessionComplexity: { simple: simpleSessions, moderate: moderateSessions, complex: complexSessions, total: totalClassified },
    guide,
    currentModels: userModels.map((m) => m.model),
  };
}

function estimateSavings(batch, affectedSessions, totalSessions, targetTier) {
  if (totalSessions === 0 || affectedSessions === 0) return null;
  const avgCost = batch.estimatedCost / totalSessions;
  const affectedCost = avgCost * affectedSessions;

  // Estimate target cost based on tier
  const tierMultiplier = targetTier === "lightweight" ? 0.15 : targetTier === "versatile" ? 0.6 : 1.0;
  const targetCost = affectedCost * tierMultiplier;
  const savings = affectedCost - targetCost;

  if (savings < 0.01) return null;
  return `~$${savings.toFixed(2)} potential savings`;
}

/**
 * Generate token-focused optimization tips based on user patterns.
 */
export function promptOptimizationTips({ repo, since, excludeIds } = {}) {
  const redirData = tokensPerRedirection({ repo, since, excludeIds });
  const styleData = tokensByWorkStyle({ repo, since, excludeIds });
  const effData = tokenEfficiencyMetrics({ repo, since, excludeIds });
  const tips = [];

  // Tip 1: Redirection waste
  if (redirData.wasteRate > 20) {
    tips.push({
      id: "reduce-redirections",
      icon: "🔄",
      title: "Reduce Redirections",
      impact: `~${redirData.wasteRate}% of tokens are spent on redirections`,
      suggestion: "Be more specific in your initial prompt. Include file paths, expected behavior, and constraints upfront to avoid course corrections.",
      savings: `~${formatTokens(redirData.redirectionTokens)} tokens could be saved`,
    });
  }

  // Tip 2: Work style optimization
  if (styleData.insight) {
    tips.push({
      id: "optimize-work-style",
      icon: styleData.cheapestStyle === "structured" ? "🎯" : "⚡",
      title: "Optimize Your Work Style",
      impact: styleData.insight,
      suggestion: styleData.cheapestStyle === "structured"
        ? "Start sessions with a planning phase — describe the full scope before implementation."
        : "Try different approaches: planning first for complex tasks, quick-start for simple ones.",
      savings: null,
    });
  }

  // Tip 3: Context drip-feeding
  if (redirData.byCategory.some((c) => c.category === "repetition" && c.count > 3)) {
    tips.push({
      id: "batch-context",
      icon: "📦",
      title: "Batch Your Context",
      impact: "You frequently add context incrementally, causing extra round-trips",
      suggestion: "Include all relevant context (files, requirements, constraints) in your first message instead of adding them across multiple turns.",
      savings: null,
    });
  }

  // Tip 4: Token ROI
  if (effData.tokenROI < 0.5 && effData.totalFileOps > 0) {
    tips.push({
      id: "improve-token-roi",
      icon: "📊",
      title: "Improve Token ROI",
      impact: `Currently ${effData.tokenROI} file ops per 1K tokens — room for improvement`,
      suggestion: "Focus sessions on clear, implementable tasks. Vague exploration burns tokens without producing file changes.",
      savings: null,
    });
  }

  // Tip 5: Productive token ratio
  if (effData.productiveTokenRatio < 70) {
    tips.push({
      id: "increase-productive-turns",
      icon: "✅",
      title: "Increase Productive Turns",
      impact: `Only ${effData.productiveTokenRatio}% of turns are productive (no redirections)`,
      suggestion: "Review your prompts before sending. Ask: 'Does this give the agent enough context to succeed on the first try?'",
      savings: null,
    });
  }

  // Tip 6: Model selection
  const expensiveModelSessions = styleData.styles.filter((s) => s.sessions > 0);
  if (expensiveModelSessions.length > 1) {
    const cheapest = expensiveModelSessions.reduce((a, b) => a.avgCostPerSession < b.avgCostPerSession ? a : b);
    const costliest = expensiveModelSessions.reduce((a, b) => a.avgCostPerSession > b.avgCostPerSession ? a : b);
    if (costliest.avgCostPerSession > cheapest.avgCostPerSession * 1.5) {
      tips.push({
        id: "model-selection",
        icon: "🤖",
        title: "Consider Model Selection",
        impact: `${costliest.emoji} ${costliest.style} sessions cost ${Math.round(costliest.avgCostPerSession / cheapest.avgCostPerSession)}× more`,
        suggestion: "Use smaller, faster models for simple tasks (quick fixes, renames) and reserve powerful models for complex, multi-file changes.",
        savings: null,
      });
    }
  }

  // Get model recommendations
  const modelRecs = modelRecommendations({ repo, since, excludeIds });

  return {
    tips,
    summary: {
      wasteRate: redirData.wasteRate,
      productiveRatio: effData.productiveTokenRatio,
      tokenROI: effData.tokenROI,
    },
    modelRecommendations: modelRecs.recommendations,
    sessionComplexity: modelRecs.sessionComplexity,
    modelGuide: modelRecs.guide,
    currentModels: modelRecs.currentModels,
  };
}

// ── Helpers ──────────────────────────────────────────────────

function formatTokens(n) {
  if (n >= 1_000_000) return `${(n / 1_000_000).toFixed(1)}M`;
  if (n >= 1_000) return `${(n / 1_000).toFixed(1)}K`;
  return String(n);
}
